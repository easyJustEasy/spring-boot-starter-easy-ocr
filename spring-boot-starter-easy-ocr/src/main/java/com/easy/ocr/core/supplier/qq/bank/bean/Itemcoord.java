package com.easy.ocr.core.supplier.qq.bank.bean;

public class Itemcoord {

    private int x;
    private int y;
    private int width;
    private int height;
    public void setX(int x) {
         this.x = x;
     }
     public int getX() {
         return x;
     }

    public void setY(int y) {
         this.y = y;
     }
     public int getY() {
         return y;
     }

    public void setWidth(int width) {
         this.width = width;
     }
     public int getWidth() {
         return width;
     }

    public void setHeight(int height) {
         this.height = height;
     }
     public int getHeight() {
         return height;
     }

}